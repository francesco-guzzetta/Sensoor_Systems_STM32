void playNote(notaT nota){

	__HAL_TIM_SET_AUTORELOAD(&htim1, nota.f-1);
	__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_2, nota.f/2-1);
	__HAL_TIM_SET_COUNTER(&htim1, 0);

	__HAL_TIM_SET_AUTORELOAD(&htim2, ((300000.0/TEMPO_BPM)*nota.d)-1);
	__HAL_TIM_SET_COUNTER(&htim2, 0);
	__HAL_TIM_CLEAR_FLAG(&htim2, TIM_FLAG_UPDATE);

	//start reproducing the note
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
	HAL_TIM_Base_Start_IT(&htim2);

	musicState = _NOTE_PLAYING;
	while(musicState == _NOTE_PLAYING){}

	HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
	HAL_TIM_Base_Stop_IT(&htim2);
}

void playSong(notaT song[], int nNote){
	for (int i = 0; i < nNote; ++i) {
		playNote(song[i]);
	}
}

/*
 * When I detect sound on the microphone and I'm not playing I change the state
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin==GPIO_PIN_8 && musicState == _NOT_PLAYING){
		musicState = _START_PLAYING;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	//timer2 ticks every 0.1ms when ticks i must stop the PWM of TIM1 and trigger next note to play
	if (htim  ==  &htim2 && musicState == _NOTE_PLAYING){
		musicState = _PLAYING;
	}
}
